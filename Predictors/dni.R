library(raster)
library(mapview)
library(sp)
library(rgeos)

setwd("/Git/Data/processingpredictors/dni")
rasterOptions(tmpdir="/Git/Data/tmp")

dnig = raster("dnig.tif")
dninl =raster("dninl.tif")
#------------------------------------------------------------------------------------------------------------------------------------
# a) Merge DNI Germany and DNI Netherlands
#------------------------------------------------------------------------------------------------------------------------------------

dni = raster::merge(dnig, dninl, tolerance=0.5)

#------------------------------------------------------------------------------------------------------------------------------------
# b) crop and resample
#------------------------------------------------------------------------------------------------------------------------------------

srtm = raster("/Git/Data/Predictors/srtm10.tif")

dni_crop <- crop(dni,srtm)

# Resample 10x10m resolution

dni_resample = resample(dni_crop,srtm, method="ngb", filename="dni10.tif")







